/*
2.Железничка станица
На железничката станица има возови со текстуални броеви на регистрација. За секој воз се чуваат информации за модел и оператор, во регистар. Регистарот овозможува пристап до податоците (модел и оператор)
за било кој број на регистрација со сложеност О(1). За да добие пристап до пругата, возот мора да има тежина стриктно помала од W тони. Вие треба да напишете програма која чита пријави на возови
(регистарски број + тежина во тони) и за секоја пријава каде тежината е помала од W да го печати моделот и операторот на соодветниот воз. Влез: Прво е даден број N - број на возови во регистарот.
Следуваат N редови со: регистарски број, модел на воз и оператор, во тој редослед. Во следниот ред е даден цел број W - максимална тежина (во тони) за пристап до пругата.
Потоа следат парови тежина и регистарска табличка, се додека не се внесе -1 closed. Излез: За секој пар од влезот, ако тежината е помала од W,
тогаш испечатете ги моделот и операторот во посебен ред. За задачата да се смета за точна треба пристапот до моделот и операторот за даден регистарски број да биде со сложеност О(1).
Можете да користите податочна структура по свој избор, која што го исполнува барањето погоре.

*/
import java.util.*;


public class TrainStation {

public static void main(String[] args) {

Scanner sc = new Scanner(System.in);


// број на возови во регистарот

int N = sc.nextInt();

sc.nextLine(); // прочистување на редот


// хеш табела: регистарски број -> (модел + оператор)

HashMap<String, String> registry = new HashMap<String,String>();


for (int i = 0; i < N; i++) {

// секој ред: регистарски број, модел, оператор

String reg = sc.next(); // регистрација

String model = sc.next(); // модел

String operator = sc.next(); // оператор


registry.put(reg, model + " " + operator);

}


int W = sc.nextInt(); // максимална дозволена тежина

sc.nextLine(); // чистење


while (true) {

String line = sc.nextLine();

if (line.equals("-1 closed")) {

break; // крај на влезот

}


String[] parts = line.split(" ");

int weight = Integer.parseInt(parts[0]);

String reg = parts[1];


if (weight < W) {

// директен пристап O(1) до податоците

System.out.println(registry.get(reg));

}

}

}

}
